      <h3>Why do we care?</h3>

      <br>

      <p>We care because we're all in this together. In these difficult and trying times, we are all facing similar struggles. We can't fix everything, but we can band together and, little by little, mask by mask, save lives. Together, we really can make a difference, and together we can support each other to get through this crisis.</p>

      <br>

      <h3>Why nursing homes and personal care facilities?</h3>

      <br>

      <p>Nursing homes and personal care facilities have been particularly hard hit by Covid-19, with very high rates of infection among residents and staff. In Pennsylvania alone, over half of deaths from COVID-19 have been residents of nursing homes and personal care homes. (citation)  At the same time, many of these facilities lack adequate protective equipment such as masks for staff and residents, compounding the problem.</p>

