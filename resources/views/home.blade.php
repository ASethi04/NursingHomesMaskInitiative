      <h4>In the midst of the COVID-19 pandemic, nursing homes and personal care facilities have been hit exceptionally hard by medical supply shortages. Without masks, residents and staff face a high chance of infection. We can help.</h4>

      <br>
      <br>
      <br>

      <h3>Our Mission</h3>

      <br>

      <p>Our mission is to save lives by helping crafters and sewers like you to make masks for nursing homes and personal care facilities in need.</p>

      <br>
      
      <h3>What Can I Do?</h3>

      <br>

      <p>We need people to make masks for nursing homes and personal care facilities, and spread the word. Whether you are an experienced home crafter or sewer, or a beginner learning from scratch, you have come to the right place. Head over to 'Participate' to find the nursing home or personal care facility in need nearest to you, and make use of the sewing resources we have gathered from all over the internet to get stitching. It's important that you spread the word. Share us with your friends to get more people sewing. The more masks we make, the more lives we can save.</p>

      <br>

      <h3>We're Constantly Updating</h3>

      <br>

      <p>As the COVID-19 situation evolves, we will be adding new resources to this site. We want to provide you with the best data and the best information to allow you to help the most people, so we will be constantly adapting to meet your needs.</p>

